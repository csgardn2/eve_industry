/// \brief Performs a lookup that returns a station ID given the name of the
/// station as a string.  This function is not case sensitive and ignores
/// nonalphanumeric characters.  Additionally, as station names can be quite
/// long, you may pass any substring of the station name to get all partial
/// matches.  Note that this function will return a vector of all matching
/// regions in the EvE galaxy, and is in no way restricted to a particular
/// region.
/// Number | Situation | Example Input str | Example Return Value
/// :----: | :------------------------------------------------------------------------------------------------------: | :--------------------------------------------------------------------------------------: | :----------------------------------------------------------------------------------------------------------------------------------:
/// 1      | If the str parameter does not match any stations, you will get an empty vector.  This is the ideal case. | "poop"                                                                                   | \<Empty Vector\>
/// 2      | If the str parameter uniquly identifies a station, you will get exactly one answer.                      | "Saisio VII - Moon 1 - Peace and Order Unit Testing Facilities" or "Saisio VII - Moon 1" | \ref station_t "SAISIOVIIMOON1PEACEANDORDERUNITTESTINGFACILITIES"
/// 3      | If the str parameter matches multiple stations, you will get a list of all the matching stations.        | "1 - Peace"                                                                              | \ref station_t "ONUSEVIIMOON11PEACEANDORDERUNITASSEMBLYPLANT" and \ref station_t "SAISIOVIIMOON1PEACEANDORDERUNITTESTINGFACILITIES"
std::vector<station_t> string_to_station(const char* str);

/// \brief Performs a lookup that returns a pretty human-readable string
/// corresponding to that station.
/// Note that region != region_to_string(string_to_region(region)) due to
/// the case insensitivity and special character removal done in \ref
/// string_to_station.  If a match is not found, then NULL is returned.
const char* station_to_string(station_t station);

